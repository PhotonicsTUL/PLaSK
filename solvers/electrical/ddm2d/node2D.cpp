#include "node2D.h"
//------------------------------------------------------------------------------
Node2D::Node2D(int iNo, double iX, double iY, char iSide)
{
	mNo = iNo;
	mX = iX;
	mY = iY;
	mSide = iSide;
	mPsi = 0.;
	mFn = 0.;
	mFp = 0.;
	mFnEta = exp(0.);
	mFpKsi = exp(0.);
	mfPsi0 = 0;
    scaleT = 300.;
    scaleN = 1e18;
    scaleEpsR = 10.;
    scaleX = sqrt((plask::phys::epsilon0*plask::phys::kB_J*scaleT*scaleEpsR)/(plask::phys::qe*plask::phys::qe*scaleN))*1e3;
    mT = 300./scaleT; // TODO uwaga
}
//------------------------------------------------------------------------------
Node2D::Node2D() { }
//------------------------------------------------------------------------------
Node2D::~Node2D() { }
//------------------------------------------------------------------------------
void Node2D::getInfo()
{
    std::cout << mNo << "\t" << getSide() << "\t" << (getX())*scaleX << "\t" << (getY())*scaleX << "\t" << mT << "\t" << mPsi << "\t" << mFn << "\t" << mFp << "\n";
}
